const getNestedValue = (object, path) =>
  path.split(".").reduce((current, key) => current?.[key], object);

const header = document.querySelector("[data-header]");
const languageToggle = document.querySelector("[data-language-toggle]");
const languageCurrent = document.querySelector("[data-language-current]");
const menuToggle = document.querySelector("[data-menu-toggle]");
const mobileNav = document.querySelector("[data-mobile-nav]");
const navLinks = document.querySelectorAll(".desktop-nav a");
const backToTop = document.querySelector("[data-back-to-top]");
const modal = document.querySelector("[data-booking-modal]");
const dialog = modal.querySelector(".booking-dialog");
const modalService = document.querySelector("[data-modal-service]");
const closeButtons = modal.querySelectorAll("[data-modal-close]");
const bookingButtons = document.querySelectorAll("[data-booking]");
const serviceCards = document.querySelectorAll("[data-service-card]");

let currentLanguage = "zh-HK";
let currentService = "general";
let lastFocusedElement = null;

try {
  const savedLanguage = window.localStorage.getItem("shiji-language");
  if (savedLanguage === "en" || savedLanguage === "zh-HK") currentLanguage = savedLanguage;
} catch {
  currentLanguage = "zh-HK";
}

const applyLanguage = (language) => {
  currentLanguage = language;
  const dictionary = translations[language];

  document.documentElement.lang = language;
  document.title = dictionary.meta.title;
  document.querySelector('meta[name="description"]').content = dictionary.meta.description;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getNestedValue(dictionary, element.dataset.i18n);
    if (typeof value === "string") element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const value = getNestedValue(dictionary, element.dataset.i18nAria);
    if (typeof value === "string") element.setAttribute("aria-label", value);
  });

  languageCurrent.textContent = language === "zh-HK" ? "EN" : "繁";
  languageToggle.setAttribute(
    "aria-label",
    language === "zh-HK" ? "Switch to English" : "切換至繁體中文"
  );
  menuToggle.setAttribute("aria-label", dictionary.accessibility.openMenu);
  modalService.textContent = dictionary.services[currentService].name;

  try {
    window.localStorage.setItem("shiji-language", language);
  } catch {
    // The language still works when storage is unavailable.
  }
};

languageToggle.addEventListener("click", () => {
  applyLanguage(currentLanguage === "zh-HK" ? "en" : "zh-HK");
});

const closeMenu = () => {
  document.body.classList.remove("menu-open");
  menuToggle.setAttribute("aria-expanded", "false");
  menuToggle.setAttribute(
    "aria-label",
    translations[currentLanguage].accessibility.openMenu
  );
};

menuToggle.addEventListener("click", () => {
  const isOpen = document.body.classList.toggle("menu-open");
  menuToggle.setAttribute("aria-expanded", String(isOpen));
  menuToggle.setAttribute(
    "aria-label",
    isOpen
      ? translations[currentLanguage].accessibility.closeMenu
      : translations[currentLanguage].accessibility.openMenu
  );
});

mobileNav.querySelectorAll("a").forEach((link) => link.addEventListener("click", closeMenu));

window.addEventListener("resize", () => {
  if (window.innerWidth > 1024) closeMenu();
});

const activateServiceCard = (activeCard) => {
  serviceCards.forEach((card) => card.classList.toggle("is-active", card === activeCard));
};

serviceCards.forEach((card) => {
  card.addEventListener("mouseenter", () => activateServiceCard(card));
  card.addEventListener("focusin", () => activateServiceCard(card));
});

const openModal = (service) => {
  currentService = translations[currentLanguage].services[service] ? service : "general";
  lastFocusedElement = document.activeElement;
  modalService.textContent = translations[currentLanguage].services[currentService].name;
  modal.classList.add("open");
  modal.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
  window.requestAnimationFrame(() => dialog.focus());
};

const closeModal = () => {
  modal.classList.remove("open");
  modal.setAttribute("aria-hidden", "true");
  document.body.classList.remove("modal-open");
  if (lastFocusedElement instanceof HTMLElement) lastFocusedElement.focus();
};

bookingButtons.forEach((button) => {
  button.addEventListener("click", () => {
    closeMenu();
    openModal(button.dataset.booking);
  });
});

closeButtons.forEach((button) => button.addEventListener("click", closeModal));

document.addEventListener("keydown", (event) => {
  if (!modal.classList.contains("open")) return;

  if (event.key === "Escape") {
    closeModal();
    return;
  }

  if (event.key !== "Tab") return;

  const focusable = [
    ...dialog.querySelectorAll(
      'a[href], button:not([disabled]), [tabindex]:not([tabindex="-1"])'
    ),
  ].filter((element) => !element.hasAttribute("hidden"));

  if (!focusable.length) return;

  const first = focusable[0];
  const last = focusable[focusable.length - 1];

  if (event.shiftKey && (document.activeElement === first || document.activeElement === dialog)) {
    event.preventDefault();
    last.focus();
  } else if (!event.shiftKey && document.activeElement === last) {
    event.preventDefault();
    first.focus();
  }
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("visible");
      revealObserver.unobserve(entry.target);
    });
  },
  { threshold: 0.1 }
);

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));

const updateScrollState = () => {
  const scrollY = window.scrollY;
  header.classList.toggle("scrolled", scrollY > 24);
  backToTop.classList.toggle("visible", scrollY > 700);

  const sections = [...document.querySelectorAll("main section[id]")];
  const currentSection = sections
    .filter((section) => section.offsetTop - 150 <= scrollY)
    .at(-1);

  navLinks.forEach((link) => {
    link.classList.toggle(
      "active",
      Boolean(currentSection) && link.hash === `#${currentSection.id}`
    );
  });
};

window.addEventListener("scroll", updateScrollState, { passive: true });
backToTop.addEventListener("click", () => window.scrollTo({ top: 0, behavior: "smooth" }));

applyLanguage(currentLanguage);
updateScrollState();
